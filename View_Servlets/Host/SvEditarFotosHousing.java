    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
         try {
            int housingId = Integer.parseInt(request.getParameter("id"));

          
            Housing housing = control.getHousing(housingId);

            if (housing != null) {
                request.setAttribute("housing", housing);
                request.getRequestDispatcher("indexHostingEditarFotos.jsp").forward(request, response);
            } else {
                response.sendRedirect("error.jsp"); // o una p√°gina de error personalizada
            }

        } catch (Exception e) {
            e.printStackTrace();
            response.sendRedirect("error.jsp");
        }
    }

